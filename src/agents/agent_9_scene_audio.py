def agent_9_scene_audio(
    scene_info: dict,
    connected_assets: list[dict],
    concept: str,
    audio_mode: str,
    existing_start_video_prompt: str = "",
    existing_end_video_prompt: str = "",
    existing_combined_video_prompt: str = "",
    existing_dialogue: str = "",
    user_instructions: str = "",
) -> SceneAudioEnhancement:
    """On-demand agent: generates dialogue, voice prompt, and updated video prompts when audio mode changes."""
    console.print(f"[bold yellow]--- Scene Audio Enhancer (Agent 9) Running — mode={audio_mode} ---[/bold yellow]")

    # Build connected assets context
    assets_text = ""
    for asset in connected_assets:
        assets_text += f"  - {asset.get('label', 'Unknown')} ({asset.get('type', '')}): {asset.get('text_prompt', '')}\n"

    # Build existing prompts section
    existing_section = f"""
EXISTING VIDEO PROMPTS (update these — do not discard, enhance):
  Start Video: {existing_start_video_prompt or '(none)'}
  End Video: {existing_end_video_prompt or '(none)'}
  Combined Video: {existing_combined_video_prompt or '(none)'}"""

    existing_dialogue_section = ""
    if existing_dialogue and existing_dialogue.strip():
        existing_dialogue_section = f"\nEXISTING DIALOGUE (keep if good, improve if needed): {existing_dialogue}"

    user_content = f"""CAMPAIGN CONCEPT: {concept}

TARGET AUDIO MODE: {audio_mode}

SCENE DETAILS:
  Scene #{scene_info.get('scene_number', '?')} — {scene_info.get('type', '')} / {scene_info.get('shot_type', '')}
  Action: {scene_info.get('action_movement', '')}
  Visual Description: {scene_info.get('visual_description', '')}

CAST IN THIS SCENE:
{assets_text or '  (none specified)'}
{existing_section}
{existing_dialogue_section}
{f"\nUSER INSTRUCTIONS (follow these closely): {user_instructions}" if user_instructions else ""}

Task: Generate dialogue, voice style prompt, and update the video prompts for {audio_mode} mode. {"For audio-native, the dialogue MUST be woven into the video prompts." if audio_mode == "audio-native" else "For talking-head, keep video prompts visual-only — the audio is separate."}"""

    best_practices = load_prompt_best_practices()
    system = SYSTEM_PROMPT
    if best_practices:
        system += f"\n\n═══ PROMPT BEST PRACTICES REFERENCE (follow these guidelines for dialogue & audio) ═══\n{best_practices}\n═══ END BEST PRACTICES ═══"

    result = call_agent_model(system, user_content, SceneAudioEnhancement)

    console.print(f"    [green]Audio enhancement for scene #{scene_info.get('scene_number', '?')} ({audio_mode})[/green]")

    return result
